"use client";

/** Labeled slider with live value display. */
export default function Slider({
  label,
  value,
  min,
  max,
  step = 1,
  format,
  onChange,
}: {
  label: string;
  value: number;
  min: number;
  max: number;
  step?: number;
  format?: (v: number) => string;
  onChange: (v: number) => void;
}) {
  return (
    <label className="block py-1.5">
      <span className="mb-1 flex items-center justify-between text-xs font-medium text-zinc-600">
        {label}
        <span className="tabular-nums text-zinc-400">{format ? format(value) : value}</span>
      </span>
      <input
        type="range"
        min={min}
        max={max}
        step={step}
        value={value}
        onChange={(e) => onChange(Number(e.target.value))}
      />
    </label>
  );
}
